from uuid import UUID, uuid4

from pydantic import BaseModel, Field
from typing import List, Optional
from datetime import datetime
from enum import Enum

class OrderStatus(str, Enum):
    UNPAID = "unpaid"
    PENDING = "pending"
    IN_PROGRESS = "in_progress"
    READY_FOR_PICKUP = "ready_for_pickup"
    COMPLETED = "completed"
    CANCELLED = "cancelled"

class OrderItem(BaseModel):
    menu_item_id: UUID = Field(default_factory=uuid4)
    quantity: int = Field(..., gt=0)  # Quantity must be greater than 0
    price_at_time: float

class Order(BaseModel):
    id: str                     # Generated by OrderService
    customer_id: str
    restaurant_id: int
    items: List[OrderItem]
    status: OrderStatus         # Generated by OrderService (starts as "unpaid")
    order_date: datetime        # Generated by OrderService
    cost_breakdown: float       # TODO: Replace with CostBreakdown when definied
    delivery_address: Optional[str] = None
    delivery_latitude: float
    delivery_longitude: float
    delivery_postal_code: str
    delivery_instructions: Optional[str] = None

class OrderCreate(BaseModel):
    customer_id: str
    restaurant_id: int
    items: List[OrderItem]
    delivery_address: Optional[str] = None
    delivery_latitude: float
    delivery_longitude: float
    delivery_postal_code: str
    delivery_instructions: Optional[str] = None

class OrderUpdate(BaseModel):
    status: Optional[OrderStatus] = None
    delivery_address: Optional[str] = None
    delivery_latitude: Optional[float] = None
    delivery_longitude: Optional[float] = None
    delivery_postal_code: Optional[str] = None
    delivery_instructions: Optional[str] = None